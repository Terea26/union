const LANGS = [
  { code: "en", label: "English", flag: "🇬🇧" },
  { code: "cs", label: "Čeština", flag: "🇨🇿" },
  { code: "de", label: "Deutsch", flag: "🇩🇪" }
];

function getLang() {
  const saved = localStorage.getItem("hu_lang");
  if (saved && I18N[saved]) return saved;
  const nav = (navigator.language || "en").slice(0, 2);
  return I18N[nav] ? nav : "en";
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.en;
  document.documentElement.lang = lang === "cs" ? "cs" : lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const k = el.getAttribute("data-i18n");
    if (dict[k] != null) el.innerHTML = dict[k];
  });
  document.querySelectorAll("[data-i18n-attr]").forEach(el => {
    el.getAttribute("data-i18n-attr").split(",").forEach(pair => {
      const [attr, key] = pair.split(":").map(s => s.trim());
      if (dict[key] != null) el.setAttribute(attr, dict[key]);
    });
  });
  localStorage.setItem("hu_lang", lang);
  document.querySelectorAll("[data-lang-option]").forEach(b =>
    b.setAttribute("aria-current", b.getAttribute("data-lang-option") === lang ? "true" : "false"));
  const cur = LANGS.find(l => l.code === lang);
  document.querySelectorAll("[data-lang-current]").forEach(el => el.textContent = cur.code.toUpperCase());
}

window.HU_I18N = { applyLang, getLang, LANGS };
window.I18N = I18N;
